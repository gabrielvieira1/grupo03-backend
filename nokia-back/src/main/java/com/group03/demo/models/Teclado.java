package com.group03.demo.models;

public class Teclado {
	
	int numeros;
	String letras;
	
	public Teclado() {
		
	}
	
	public int getNumeros() {
		return numeros;
	}
	public void setNumeros(int numeros) {
		this.numeros = numeros;
	}
	public String getLetras() {
		return letras;
	}
	
	public void setLetras(String letras) {
		this.letras = letras;
	}
	

}
